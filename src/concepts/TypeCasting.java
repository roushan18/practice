package concepts;
//Type casting is possible among Primitive data types
public class TypeCasting {

	public static void main(String[] args) {
		// narrowing- conversion of bigger data type to smaller data type
		int i=(int) 6.8;
		System.out.println(i);
		//Widening- Conversion of smaller data types to bigger data type.
		double d= 5;
		System.out.println(d);
		

	}

}
